#include "libft.h"

void			ft_putnbr(int n)
{
	long int		nbr;

	nbr = n;
	if (nbr < 0)
	{
		write(1, "-", 1);
		nbr = nbr * -1;
	}
	while (nbr / 10 > 0)
	{
		ft_putnbr(nbr / 10);
		nbr %= 10;
	}
	ft_putchar(nbr + 48);
}
